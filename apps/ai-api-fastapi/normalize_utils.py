def normalize_exercise_name(name: str) -> str:
    synonyms = {
        # 푸시업 계열
        "푸쉬업": "푸시업",
        "푸시업 바 푸쉬업": "푸시업",
        "푸쉬업 바 푸쉬업": "푸시업",
        "푸시업 바": "푸시업",
        "푸쉬업 바": "푸시업",
        # 런닝 계열
        "러닝": "런닝",
        "달리기": "런닝",
        "뛰기": "런닝",
        # 워킹 계열
        "걷기": "워킹",
        "조깅": "워킹",
        # 런지 유사
        "워킹 런지": "런지",
        "런지 워킹": "런지",
        # 복부 운동 유사
        "바이시클 크런치": "크런치",
        "v업": "브이업",
        "V업": "브이업",
        # 기타 유사
        "버피": "버피 테스트",
        "점핑 잭": "점핑잭",
        "점핑잭": "점핑잭",
        "페이스 풀": "페이스풀",
        "업라이트로우": "업라이트 로우",
        "오버헤드 익스텐션": "오버헤드 트라이셉 익스텐션",
        "숄더프레스": "숄더 프레스"
    }
    normalized = synonyms.get(name.strip(), name.strip())
    return normalized

if __name__ == "__main__":
    test_cases = [
        "푸쉬업 바 푸쉬업",
        "달리기",
        "걷기",
        "워킹 런지",
        "바이시클 크런치",
        "점핑 잭",
        "업라이트로우",
        "숄더프레스",
        "아무거나"
    ]
    for name in test_cases:
        print(f"{name} -> {normalize_exercise_name(name)}") 